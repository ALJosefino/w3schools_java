
package javamethodsparameters02;

public class JavaMethodsParameters02 {

    
    static int myMethod(int x, int y) {
    
        return x + y;
  }   
        
        public static void main(String[] args) {
    
            System.out.println(myMethod(5, 3));
  }

    // Outputs 8 (5 + 3)
        
    }
